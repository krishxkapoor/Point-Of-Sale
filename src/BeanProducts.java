public class BeanProducts {
    public String name;
    public String catname;
    public String description;
    public int price;
    public int quantity;
    public String photo;

    public BeanProducts(String name ,String catname, String description, int price, int quantity, String photo) {
        this.name = name;
        this.catname=catname;
        this.description = description;
        this.price = price;
        this.quantity = quantity;
        this.photo = photo;
    }    
}
